# For loops
print("For string: ")
for item in 'Zero to Mastery':
    print(item)

print("\nFor List: ")
for item in [1, 2, 3, 4, 5]:
    print(item)

print("\nFor Tuple: ")
for item in (1, 2, 3, 4, 5):
    print(item)

print("\nFor Set: ")
for item in {1, 2, 3, 4, 5}:
    print(item)
print(item)

# Nested Loop
for item in [1, 2, 3, 4, 5]:
    for x in ['a', 'b', 'c']:
        print(item, x)
